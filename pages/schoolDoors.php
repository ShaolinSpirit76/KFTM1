  <h3>Histoire de l'école</h3>



  <p>Jean Marie Levray, élève et instructeur de l'école du Pontet ayant dû quitter le Vaucluse pour ses activités professionnelles, s'installait à Montivilliers. Après avoir vainement cherché une école de kung-fu sérieuse pour continuer sa pratique, la décision fut prise de créer une école. </p>



  <figure class="wp-block-image"><img src="images/61570_653762877998371_1145523459_n.jpg" alt="" class="" sizes="(max-width: 960px) 100vw, 960px" /><figcaption><small>Photo de notre école avant les travaux titanesques que nous avons réalisés.</small></figcaption></figure>



  <figure class="wp-block-image"><img src="images/1795748_653763044665021_222987431_n.jpg" alt="" class="" sizes="(max-width: 960px) 100vw, 960px" /><figcaption><small>L'école a été créée en 1996.</small></figure>



 


  <p>1996 - 2003</p>



  <p>En 1996, les premiers cours se déroulent avec 5 élèves. Ils sont dirigés par Jean Marie et son fils Julien qui le seconde. Les entraînements se déroulent dans un gymnase &#8211; avant chaque cours, il faut descendre les tatamis qui sont logés à 3 m de hauteur (c&rsquo;est le début de l&rsquo;échauffement). Il faut les remettre en place à la fin de chaque cours.</p>



  <p>L&rsquo;année 1997 se fait également dans ce gymnase, en alternance avec deux autres gymnases ; difficile de gérer les déplacements. De nouveaux élèves arrivent, tous ne resteront pas. C&rsquo;est en 1998 que nous prenons en location une ancienne menuiserie, pleine de charme. D&rsquo;autres élèves arrivent, puis comme d&rsquo;habitude, la sélection naturelle se fait d&rsquo;elle-même. L&rsquo;année 1999 est mouvementée, mais riche d&rsquo;enseignements. En septembre, après de nombreux travaux, notre salle est prête à recevoir tous les élèves des trois dernières années, ainsi qu&rsquo;un nouveau cru 99/2000 riche en quantité.</p>



  <ul class="wp-block-gallery columns-3 is-cropped"><li class="blocks-gallery-item"><figure><img src="images/travaux.jpg" alt="" data-id="111" data-link="portes-de-lecole/travaux" class="wp-image-111" sizes="(max-width: 960px) 100vw, 960px" /></figure></li><li class="blocks-gallery-item"><figure><img src="images/travaux2.jpg" alt="" data-id="112" data-link="portes-de-lecole/travaux2" class="wp-image-112" sizes="(max-width: 960px) 100vw, 960px" /></figure></li><li class="blocks-gallery-item"><figure><img src="images/travaux3.jpg" alt="" data-id="113" data-link="portes-de-lecole/travaux3" class="wp-image-113" sizes="(max-width: 960px) 100vw, 960px" /></figure></li></ul>



  <p>Notre salle est superbe &#8211; à la suite des travaux réalisés par notre instructeur, les élèves, mais aussi le propriétaire. Elle paraît prête à aborder la nouvelle saison. Malheureusement, la nature en a voulu autrement, et cette fin d&rsquo;année 99 est une catastrophe. La pluie s&rsquo;est acharnée sur Montivilliers comme sur beaucoup d&rsquo;autres régions, et elle a plongé notre salle dans prés d&rsquo;un mètre d&rsquo;eau. Un désastre, les tatamis flottaient sur une eau boueuse, de quoi se décourager.</p>



  <p>Beaucoup d&rsquo;écoles de toutes sortes auraient fermé leurs portes à la suite d&rsquo;une telle épreuve, mais la nôtre est encore là et a retrouvé tout le charme d&rsquo;une salle de kung-fu que beaucoup d&rsquo;enseignants aimeraient avoir. Et tout cela grâce aux efforts déployés par notre instructeur, sa famille, les élèves et le propriétaire. Une grande partie de la rénovation a été financièrement possible grâce à la vente des calendriers à l&rsquo;effigie de l&rsquo;école. Toutes les écoles Thieulâm ont manifesté leur solidarité à cette occasion. À ce moment, notre école accueille prés de 70 élèves. Nous voyons l&rsquo;avenir sereinement, et pensons que si notre école a pu résister à cette épreuve imposée par la nature, elle est sur la bonne voie.</p>



  <p>2003</p>



  <p>Décidément , notre école est destinée à subir des épreuves. Au mois de mai 2003, alors qu&rsquo;une grande partie de nos élèves se trouve au Pontet pour le Tournoi national Adultes, une nouvelle catastrophe s&rsquo;abat sur nous. Une nouvelle inondation, encore plus sévère que la première, dévaste nos locaux. Nous apprenons la nouvelle, impuissants, alors que nos compétiteurs sont en train d&rsquo;évoluer. Profondément choqués et perturbés, notre équipe récolte malgré tout son lot de médailles. L&rsquo;ensemble des écoles Thieulâm nous ayant assuré de leur compassion et de leur solidarité, c&rsquo;est avec un certain envoûtement que nous repartons vers Montivilliers. Les dégâts sont à la mesure de notre imagination. Les dures journées de nettoyage commencent&#8230; Pratiquement tout le matériel est irrécupérable, et nous devons jeter les tatamis, le matériel de frappe, les meubles… Même les archives sont perdues…</p>



